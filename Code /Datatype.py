# Integer
num_integer = 42

# Float
num_float = 3.14

# String
str_text = "Hello, World!"

# Boolean
bool_true = True
bool_false = False

# List (mutable)
list_numbers = [1, 2, 3, 4, 5]
list_names = ["Alice", "Bob", "Charlie"]
list_mixed = [1, "two", 3.0, True]

# Tuple (immutable)
tuple_coordinates = (10, 20)
tuple_colors = ("red", "green", "blue")

# Set (mutable, unordered, unique elements)
set_fruits = {"apple", "banana", "orange"}

# Dictionary (mutable, key-value pairs)
dict_person = {"name": "Alice", "age": 30, "city": "New York"}

# Displaying data types
print("Data Types:")
print("Integer:", num_integer)
print("Float:", num_float)
print("String:", str_text)
print("Boolean (True):", bool_true)
print("Boolean (False):", bool_false)
print("List:", list_numbers)
print("Tuple:", tuple_coordinates)
print("Set:", set_fruits)
print("Dictionary:", dict_person)
